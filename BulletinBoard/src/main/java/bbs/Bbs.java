package bbs;

public class Bbs {

	private int bbsIndex;
	private String bbsTitle;
	private String bbsContent;
	private String bbsAuthor;
	private String bbsDate;
	private int availableInt;
	
	public int getBbsIndex() {
		return bbsIndex;
	}
	public void setBbsIndex(int bbsIndex) {
		this.bbsIndex = bbsIndex;
	}
	public String getBbsTitle() {
		return bbsTitle;
	}
	public void setBbsTitle(String bbsTitle) {
		this.bbsTitle = bbsTitle;
	}
	public String getBbsContent() {
		return bbsContent;
	}
	public void setBbsContent(String bbsContent) {
		this.bbsContent = bbsContent;
	}
	public String getBbsAuthor() {
		return bbsAuthor;
	}
	public void setBbsAuthor(String bbsAuthor) {
		this.bbsAuthor = bbsAuthor;
	}
	public String getBbsDate() {
		return bbsDate;
	}
	public void setBbsDate(String bbsDate) {
		this.bbsDate = bbsDate;
	}
	public int getAvailableInt() {
		return availableInt;
	}
	public void setAvailableInt(int availableInt) {
		this.availableInt = availableInt;
	}

}
